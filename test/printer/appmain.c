        case d_OK:
            CTOS_PrinterPutString("d_OK");
            break;
        case d_PRINTER_HEAD_OVERHEAT:
            CTOS_PrinterPutString("d_PRINTER_HEAD_OVERHEAT");
            break;
        case d_PRINTER_PAPER_OUT:
            CTOS_PrinterPutString("d_PRINTER_PAPER_OUT");
