    FONT_ATTRIB.Y_Space = 0;  //The Height of the space between the font with next font //
    
    memset(baTemp,0x00,sizeof(baTemp));
    //Copy the "CASTLES" Graphic Logo to a Temp //
    for (i=0;i<7;i++){	 //Hight/8
        for (j=0;j<60;j++){  //Width
            baTemp[a+j] = baPrinterBufferLogo_Single[b];
            b++;
        }
        a = a+384;
    }
    
    //Put a string into the printer buffer //
    CTOS_PrinterBufferPutString((BYTE *)baTemp,61,13,"CASTLES Technology",&FONT_ATTRIB);
    //The printer will print out an image which inside the Printer Buffer.
    CTOS_PrinterBufferOutput((BYTE *)baTemp,8); //Print the "CASTLES" Graphic & Text Logo //
